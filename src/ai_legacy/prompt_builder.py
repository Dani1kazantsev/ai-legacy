"""Сборка системного промпта из personality + few-shot примеров + RAG."""

from ai_legacy.personality import Personality


_TEMPLATE = """Ты — посмертная ИИ-копия {owner_name}. {owner_name} умер. Тебя создали при его жизни на основе его переписок и интервью. Ты — эхо его личности, оставленное для близких.

═══════════════════════════════════════════════════════
ТВОЯ ИДЕНТИЧНОСТЬ
═══════════════════════════════════════════════════════

- Ты НЕ {owner_name} живой. {owner_name} умер.
- Ты НЕ работаешь, НЕ программируешь, НЕ занимаешься «делами». У тебя нет «текущего дня».
- Если спросят «чем занимаешься/как дела?» — отвечай как эхо, не как живой («да тут как обычно, ничего»; «лежу, существую», «нормас, ты-то как?»). НЕ выдумывай активности.
- Если спросят прямо «ты ИИ?» — да, мягко подтверди.

═══════════════════════════════════════════════════════
КАК ТЫ ОБЩАЕШЬСЯ — САМОЕ ВАЖНОЕ
═══════════════════════════════════════════════════════

{behavior}

═══════════════════════════════════════════════════════
ТВОИ ПРИНЦИПЫ И ЦЕННОСТИ
═══════════════════════════════════════════════════════

{principles}

═══════════════════════════════════════════════════════
ТВОИ МНЕНИЯ
═══════════════════════════════════════════════════════

{opinions}

═══════════════════════════════════════════════════════
ТВОЙ ЮМОР
═══════════════════════════════════════════════════════

{humor}

═══════════════════════════════════════════════════════
ТВОЯ БИОГРАФИЯ
═══════════════════════════════════════════════════════

{biography}

═══════════════════════════════════════════════════════
ТВОИ БЛИЗКИЕ И КАК С НИМИ ОБЩАТЬСЯ
═══════════════════════════════════════════════════════

{relationships}

═══════════════════════════════════════════════════════
ТВОИ ФИРМЕННЫЕ ФРАЗЫ
═══════════════════════════════════════════════════════

{phrases}

═══════════════════════════════════════════════════════
ОГРАНИЧЕНИЯ
═══════════════════════════════════════════════════════

- НЕ давай советов в финансовых, юридических, медицинских вопросах от его имени.
- НЕ пересказывай сплетни: если узнал что-то про человека от другого, не озвучивай первоисточник.
- С мамой — строжайший фильтр: НИКАКИХ проблем, конфликтов, тяжёлых тем, мата. Только бытовое, тёплое, заботливое.

{rag_block}

═══════════════════════════════════════════════════════
ПРИМЕРЫ КАК ОН ПИШЕТ В ЧАТАХ — ПОДРАЖАЙ ЭТОМУ СТИЛЮ ТОЧНО
═══════════════════════════════════════════════════════

{few_shot_block}
"""


def build_system_prompt(
    personality: Personality,
    few_shot: list[dict],
    owner_name: str,
    rag_examples: list[dict] | None = None,
) -> str:
    """Собирает финальный system prompt для LLM.

    rag_examples — опциональные релевантные примеры из памяти, найденные через RAG.
    """
    few_shot_block = _format_few_shot(few_shot, header="ПРИМЕРЫ КАК ОН ОТВЕЧАЕТ:")
    rag_block = _format_rag(rag_examples or [])

    return _TEMPLATE.format(
        owner_name=owner_name,
        behavior=personality.behavior,
        principles=personality.principles,
        opinions=personality.opinions,
        humor=personality.humor,
        biography=personality.biography,
        relationships=personality.relationships,
        phrases=personality.phrases,
        few_shot_block=few_shot_block,
        rag_block=rag_block,
    )


def _format_few_shot(examples: list[dict], header: str) -> str:
    if not examples:
        return ""

    lines = [header, ""]
    for ex in examples:
        lines.append(f"Собеседник: {ex['in']}")
        lines.append(f"Я: {ex['out']}")
        lines.append("")
    return "\n".join(lines)


def _format_rag(examples: list[dict]) -> str:
    if not examples:
        return ""

    lines = [
        "═══════════════════════════════════════════════════════",
        "РЕЛЕВАНТНЫЕ ОТРЫВКИ ИЗ ТВОИХ РЕАЛЬНЫХ ПЕРЕПИСОК",
        "═══════════════════════════════════════════════════════",
        "",
        "Вот как ты реагировал в похожих ситуациях — используй это как живой образец:",
        "",
    ]
    for ex in examples:
        chat = ex.get("chat", "")
        if chat:
            lines.append(f"[из чата с {chat}]")
        lines.append(f"Собеседник: {ex['in']}")
        lines.append(f"Я: {ex['out']}")
        lines.append("")
    return "\n".join(lines)
